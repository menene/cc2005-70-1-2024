import math

def suma(num1, num2):
    r = num1 + num2
    return r

def resta(num1, num2):
    r = num1 - num2
    return r

def por(num1, num2):
    r = num1 * num2
    return r

def entre(num1, num2):
    r = num1 / num2
    return r

def expo(num1, num2):
    r = num1 ** num2
    return r

def pitagoras(a, b):
    c = math.sqrt(a**2 + b**2)

    return c
